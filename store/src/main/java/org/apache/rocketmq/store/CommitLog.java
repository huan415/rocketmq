    public SelectMappedBufferResult getData(final long offset, final boolean returnFirstOnNotFound) {
        int mappedFileSize = this.defaultMessageStore.getMessageStoreConfig().getMappedFileSizeCommitLog();
        MappedFile mappedFile = this.mappedFileQueue.findMappedFileByOffset(offset, returnFirstOnNotFound);
        if (mappedFile != null) {
            int pos = (int) (offset % mappedFileSize);
            SelectMappedBufferResult result = mappedFile.selectMappedBuffer(pos);
            return result;
        }

        return null;
    }

    /**
     * When the normal exit, data recovery, all memory data have been flush
     */
    //yangyc-main Broker 上次正常关机， 启动时，恢复 CommitLog: 1. mappedFileQueue 的刷盘位点  2.mappedFileQueue当前正在顺序写的 mappedFile 的 wrotePosition、flushedPosition
    //yangyc 上次关机属于正常关机时，执行的恢复方法。参数:恢复阶段ConsumeQuue 中已知的最大消息 offset （存储主模块启动阶段，先恢复所有的consumeQueue数据，再恢复CommitLog）
    public void recoverNormally(long maxPhyOffsetOfConsumeQueue) {
        //yangyc 是否检查 CRC。 默认：true
        boolean checkCRCOnRecover = this.defaultMessageStore.getMessageStoreConfig().isCheckCRCOnRecover();
        final List<MappedFile> mappedFiles = this.mappedFileQueue.getMappedFiles();
        if (!mappedFiles.isEmpty()) { //yangyc 不为空，进入恢复逻辑
            // Began to recover from the last third file
            //yangyc-main 倒数第三个，开始向后恢复
            int index = mappedFiles.size() - 3;
            if (index < 0)
                index = 0; //yangyc 所有的文件数小于3个，则从第一个开始恢复

            MappedFile mappedFile = mappedFiles.get(index); //yangyc 获取待恢复的 mappedFile
            ByteBuffer byteBuffer = mappedFile.sliceByteBuffer(); //yangyc 获取待恢复的 mappedFile 的 mappedByteBuffer 的切片（包含全部文件的数据）
            long processOffset = mappedFile.getFileFromOffset(); //yangyc 获取待恢复 mappedFile 的文件名。 为起始偏移量（目录位点）
            long mappedFileOffset = 0; //yangyc 待处理 mappedFile 的处理位点（文件位点，从0开始）
            while (true) {
                //yangyc-main 从切片内解析出来一条 msg, 并封装成 dispactchRequest。特殊情况：1.magic_code表示文件尾，2.request.size=-1 文件内的所有消息都处理完了，并且还没有到达文件尾
                //yangyc-main CASE1: 检查 dispactchRequest 对象状态, 如果是正常数据, 则 mappedFileOffset += request.size
                //            CASE2: 检查 dispactchRequest 对象状态, 如果是文件末尾, 则 处理下一个文件, mappedFileOffset 归 0
                DispatchRequest dispatchRequest = this.checkMessageAndReturnSize(byteBuffer, checkCRCOnRecover);
                int size = dispatchRequest.getMsgSize();
                // Normal data
                if (dispatchRequest.isSuccess() && size > 0) {
                    mappedFileOffset += size; //yangyc-main 正常消息走这里
                }
                // Come the end of the file, switch to the next file Since the
                // return 0 representatives met last hole,
                // this can not be included in truncate offset
                else if (dispatchRequest.isSuccess() && size == 0) {//yangyc-main 文件尾的情况走这里
                    index++;
                    if (index >= mappedFiles.size()) {
                        // Current branch can not happen
                        log.info("recover last 3 physics file over, last mapped file " + mappedFile.getFileName());
                        break;
                    } else {
                        mappedFile = mappedFiles.get(index); //yangyc 获取下一个 mappedFile
                        byteBuffer = mappedFile.sliceByteBuffer(); //yangyc 获取这个 mappedFile 的切片
                        processOffset = mappedFile.getFileFromOffset();//yangyc 获取待恢复 mappedFile 的文件名。 为起始偏移量（目录位点）
                        mappedFileOffset = 0; //yangyc 处理位点归 0
                        log.info("recover next physics file, " + mappedFile.getFileName());
                    }
                }
                // Intermediate file read error
                else if (!dispatchRequest.isSuccess()) {
                    log.info("recover physics file end, " + mappedFile.getFileName());
                    break;
                }
            }
            //yangyc-main 执行到这里，说明待恢复的数据已经检查了一遍了
            //yangyc-main 旧processOffset:最后一个文件的文件名的值；mappedFileOffset:待处理 mappedFile 的处理位点。加起来等于新的processOffset:commitLog全局位点
            processOffset += mappedFileOffset;
            this.mappedFileQueue.setFlushedWhere(processOffset); //yangyc-main 设置mfq的刷盘位点
            this.mappedFileQueue.setCommittedWhere(processOffset);
            //yangyc-main 删除 consumeQueue 下的脏文件，根据上一步计算的 phyOffset 值, 将超过该值的 ConsumeQueue 下的文件删除，将包含该值的 ConsumeQueue 下的 mappedFile 重新定位 WrotePosition、FlushedPosition
            this.mappedFileQueue.truncateDirtyFiles(processOffset); //yangyc 调整 mfq 内当前正在顺序写的 mappedFile 的刷盘点和写入点

            // Clear ConsumeQueue redundant data
            if (maxPhyOffsetOfConsumeQueue >= processOffset) {
                log.warn("maxPhyOffsetOfConsumeQueue({}) >= processOffset({}), truncate dirty logic files", maxPhyOffsetOfConsumeQueue, processOffset);
                this.defaultMessageStore.truncateDirtyLogicFiles(processOffset); //yangyc-main 删除 consumeQueue 下的脏文件
            }
        } else {
            // Commitlog case files are deleted
            log.warn("The commitlog files are deleted, and delete the consume queue files");
            this.mappedFileQueue.setFlushedWhere(0);
            this.mappedFileQueue.setCommittedWhere(0);
            this.defaultMessageStore.destroyLogics();
        }
    }

